                    <h2 class="mt-5">PVP</h2>
                </div>
                <article class="mb-5">
                    <h5>1. Remove FOB When Done Raiding (24 Hour Rule)</h5>
                    <p>
                        Destroy your FOB after you are done with them! If an admin has to do this we may use a
                        command which will wipe all your structures, you have been warned!! Fobs left at someones
                        base for more than 24 hours without action must be removed (the defender can request an admin remove these if you do not).
                    </p>
                </article>
                <article class="mb-5">
                    <h5>2. Do Not Raid The Same Tribe Twice In 24 Hours</h5>
                    <p>
                        Do not attempt to raid the same person twice in a 24 hour period, this is considered griefing. Give them time to lick their wounds. Be reasonable!
                        Once you retreat you may not attempt another attack for 24 hours. You may leave your fob there when you go offline in the middle of a raid but
                        remember if it gets destroyed you can’t attack for 24 hours. The ONLY exception to this rule is that if someone runs with loot during a raid
                        the 24 hour rule can be ignored, or if the tribe you tried to raid firstly tries to raid you back within the 24 hours, at this point the 24
                        rule is null and can be ignored.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>3. Do Not Spam Forcefield Settings</h5>
                    <p>
                        Spamming forcefields is not allowed while being raided (big then small then big again over and over).
                    </p>
                </article>
                <article class="mb-5">
                    <h5>4. Max of 5 Soaking Turrets At Once</h5>
                    <p>
                        Mass soaking is not allowed, 5 dinos soaking turrets at once per raid is the maximum allowed. Anyone caught breaking this rule will be
                        punished for causing unnecessary server performance issues. For this rule to be broken the dinos must actively be soaking the turrets.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>5. Do Not Soak Turrets While Under NPP</h5>
                    <p>
                        No soaking bases while you or them have NPP, you can check this by trying to damage a structure (even sniping it will test this).
                    </p>
                </article>
                <article class="mb-5">
                    <h5>6. Only 1 Tribe Can Raid And Only 1 Tribe Can Defend</h5>
                    <p>
                        Allies are disabled and you may only have 6 tribe members, teaming beyond this is forbidden and all raids and defending should be
                        1 tribe vs 1 tribe. If we suspect that a friend of yours is countering someone fobbing you then we will punish you or them or both,
                        the same go’s for helping to attack. YOU HAVE BEEN WARNED. Keep it fair guys.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>7. Only 1 FOB Allowed</h5>
                    <p>
                        You may only fob 1 tribe at a time! Also you may only have 1 fob when raiding someone, multiple fobs will only cause hassle. You may move
                        your fob at any time during the raid but this must be done swiftly to avoid reports.
                    </p>
                </article>
                <article class="mb-5 pb-5">
                    <h5>8. Covering A Rider <span class="fst-italic">IS</span> Okay, However...</h5>
                    <p>
                        Covering a rider on a platform is ok however please do not build XL walls on them. This will only give us the wrong
                        idea that you are attempting to exploit. Tunnelling off dinos is a no also.
