                        Racism and toxicity in the chat is unacceptable and you can be punished for this. Banter and fighting talk is ok of course and we encourage this.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>3. Unique Survivor / Tribe name</h5>
                    <p>
                        Your survivor and tribe name has to be unique. No usage of 123, 123456789, Human, smiley faces, invisible,
                        odd characters etc. An admin will change this manually if you do not and we get creative so be sensible.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>4. Same Tribe Across Cluster</h5>
                    <p>
                        You must be in the same tribe across the cluster, you may leave your tribe to join another to assist them with
                        raiding or defending but once again you must be in that same tribe across the cluster and we make no exceptions.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>5. No Line of Sight Exploiting</h5>
                    <p>
                        Exploiting line of sight bugs (tunnel building using XL walls is an example of this) is considered cheating/exploiting
                        and will be dealt with accordingly. The same applies for tunnel building on platforms.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>6. No Griefing</h5>
                    <p>
                        Griefing someone is not ok, this is a form of bullying and we will not tolerate it.
                        Harassing someone every single day in chat, purposely killing all their dinos daily without the intention of raiding them,
                        raiding them every other day, the list goes on. Other examples are, spamming structures preventing someone building while you or them have NPP purposely or fobbing someone while you or them have NPP. Killing fresh spawns and new players, camping fresh spawn points.
                        If you think someone is griefing please create a ticket on discord and explain the situation.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>7. No Causing Lag / Affecting Server Performance</h5>
                    <p>
                        Anyone caught doing anything that affects server performance and causes massive lag will be dealt with, examples are
                        (mass stacking dinos on a single spot, mass soaking etc etc)
                    </p>
                </article>
                <article class="mb-5 pb-5">
                    <h5>8. No Insiding</h5>
                    <p>
                        Insiding is a shitty move. However it is 100% your responsibility who you allow into your tribe and what level of access
                        you give them. Anything you farm/tame/build with your time spent in that tribe belongs to that tribe, you may not take
                        these things with you. If you want to report a situation where someone has insided you please create a ticket, however as
                        a warning we wont always take action as once again it is your responsibility who you allow into your tribe. The same also
                        applies in reverse, as an example, if you join a tribe and the tribe leader kicks you without reason and such, we cant do anything about this.
                    </p>
                </article>
