            <section id="Building" class="container-fluid rounded-4 bg-dark-purple m-1 pt-1 px-5">
                <div class="mt-5 mb-5 pt-5 text-center">
                    <h2>Building</h2>
                </div>
                <article class="mb-5">
                    <h5>1. Build Anywhere Except Near Obelisks / Player Spawns</h5>
                    <p>
                        You may build anywhere (including artifact caves) apart from near/at obelisks and player spawns. Building random turret structures
                        around the map is also not allowed. Rat hole building is ok as long as it is raidable/soakable using dinos. You must not glitch into
                        spots to build we can ban or/and tribe wipe you for this! Please do not build into texture glitches on the map. These are classed as
                        under meshing and will be dealt with at admin discretion.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>2. No Excessive Spam Around Base</h5>
                    <p>
                        No excessive spamming of structures around your base. If an admin makes a request for you to reduce spam, please comply. Spamming thousands
                        of foundations is just silly and only creates client and server performance issues with render.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>3. No Invisible Structures</h5>
                    <p>
                        Do not use the invisible setting on structures you can be structure wiped for this (examples are opening every side of vacuums to make
                        floating towers under water although this is allowed INSIDE your underwater base of course). At least one side of the vacuum must be
                        closed so it can be destroyed. You can make floating vacuum towers underwater we allow these but once again none must be invisible.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>4. Only 2 Bases Per Map (And Only One Can Be In A Cave / Rathole)</h5>
                    <p>
                        2 bases per tribe allowed per map, however, only 1 may be inside of a cave. Breeding locations are also classed as bases. Any structures
                        inside a cave = base.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>5. No Using NPP To Block Others From Building</h5>
                    <p>
                        You must not block someone building if you or them have npp, examples are spamming structures despite someone already claiming a spot which
                        would prevent them building.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>6. Claiming A Spot</h5>
                    <p>
                        Spamming down foundations will claim you a spot but if someone requests removal of these to readily build in this spot an admin will remove these
                        so the player/tribe can build. Try and build at least a starter base in this spot to avoid this happening.
                    </p>
                </article>
                <article class="mb-5">
                    <h5>7. No Random Turrets</h5>
                    <p>
                        No turrets at fresh player spawns, this is a form of griefing. The same applies for troll public tp (free loot etc) to murder people with turrets,
                        turrets at drops, random placed turrets serving no purpose other than to grief etc. Admins will delete these if we think they are used for griefing,
                        if its excessive we will tribe wipe all your structures YOU HAVE BEEN WARNED..
                    </p>
                </article>
                <article class="mb-5 pb-5">
                    <h5>8. No Hiding Teleporters Inside Bases</h5>
                    <p>
                        Hidden teleporters inside someone’s cave is a no, this will only have the tribe accuse you of under mesh and so creates time consuming work for admins and
                        could result in your tribe getting banned if we suspect undermeshing to raid.
                    </p>
                </article>
